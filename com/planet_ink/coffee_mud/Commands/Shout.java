public class Shout extends Say
{
	public Shout()
	{
	}

	private final String[] access=I(new String[]{"SHOUT","SHOUTTO","SHOUTAT"});
	@Override
	public String[] getAccessWords()
	{
		return access;
	}

	@Override
	public boolean execute(final MOB mob, final List<String> commands, final int metaFlags)
		throws java.io.IOException
	{
		boolean toFlag=false;
		String verb=L("Shout");
		String averb=L("shout(s)");
		String tverb=L("shout(s) to");
		if(commands.size()>0)
		{
			for(int i=0;i<commands.size();i++)
				commands.set(i, commands.get(i).toUpperCase().trim());
			final String theCommand = commands.get(0);
			if(theCommand.endsWith("TO"))
				toFlag=true;
			else
			if(theCommand.endsWith("AT"))
			{
				tverb=averb + L("shout(s) at");
				toFlag=true;
			}
		}
		return exec(mob, toFlag, true, commands, verb, averb, tverb, metaFlags);
	}

	@Override
	public double combatActionsCost(final MOB mob, final List<String> cmds)
	{
		return CMProps.getCommandCombatActionCost(ID());
	}

	@Override
	public double actionsCost(final MOB mob, final List<String> cmds)
	{
		return CMProps.getCommandActionCost(ID());
	}

	@Override
	public boolean canBeOrdered()
	{
		return true;
	}

}
